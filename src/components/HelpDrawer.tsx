import { useEffect } from 'react';

type Props = {
  open: boolean;
  onClose: () => void;
};

export function HelpDrawer({ open, onClose }: Props) {
  useEffect(() => {
    if (!open) return;
    const onKey = (e: KeyboardEvent) => {
      if (e.key === 'Escape') onClose();
    };
    window.addEventListener('keydown', onKey);
    return () => window.removeEventListener('keydown', onKey);
  }, [open, onClose]);

  return (
    <>
      {open && <div className="help-drawer-backdrop" onClick={onClose} />}
      <aside
        className={`help-drawer${open ? ' open' : ''}`}
        aria-hidden={!open}
        role="dialog"
        aria-label="사용 안내"
      >
        <div className="help-drawer-head">
          <h3>사용 안내</h3>
          <button
            className="help-drawer-close"
            onClick={onClose}
            aria-label="도움말 닫기"
          >
            ✕
          </button>
        </div>
        <div className="help-drawer-body">
          <h4>이 도구는 무엇인가요?</h4>
          <p>
            개인정보처리방침 문서를 고치기 위한 전용 편집기입니다. 기존 방침
            원본 Word 파일(<code>.docx</code>)이나 이전에 저장해둔 구조화된
            데이터(<code>.json</code>)를 불러와서, 화면 안에서 내용을 바로 수정하고
            다시 원하는 형식으로 내려받을 수 있습니다. 수정한 결과가 실제 배포
            화면에서 어떻게 보일지 바로 옆 미리보기에 즉시 나타나기 때문에, 감으로
            작업하지 않아도 됩니다.
          </p>

          <h4>기본 작업 순서</h4>
          <p>
            먼저 툴바 왼쪽의 <strong>"파일 열기…"</strong> 버튼을 누르거나 파일을
            에디터 창 위로 끌어다 놓으세요. <code>.docx</code> 파일이면 에디터가
            자동으로 구조를 읽어서 섹션·문단·표로 분해해주고,{' '}
            <code>.json</code> 파일이면 이전에 저장해둔 모양 그대로 복원합니다.
          </p>
          <p>
            왼쪽 편집 패널에서 내용을 고치는 동안 오른쪽 미리보기가 즉시
            업데이트됩니다. 다 고쳤으면 툴바 오른쪽의 <strong>저장 버튼</strong>{' '}
            또는 바로 옆의 <strong>▾</strong> 드롭다운에서 원하는 출력 형식을
            고르세요. 지원하는 형식은 <strong>JSON</strong>,{' '}
            <strong>DOCX</strong>, <strong>Markdown</strong>이고 별도로{' '}
            <strong>인쇄 / PDF</strong> 항목이 있습니다. 어느 형식을 써야 할지는
            전달 대상이나 이후 작업 맥락에 따라 자유롭게 고르시면 됩니다.
          </p>

          <h4>섹션과 라인의 구조</h4>
          <p>
            문서는 <strong>섹션(조)</strong> 단위로 나뉘어 있습니다. 예를 들어
            "제1조 (개인정보의 처리 목적)" 이 하나의 섹션입니다. 각 섹션 안에는{' '}
            <strong>라인</strong>들이 순서대로 들어있는데, 한 문단짜리 안내문이
            라인 한 개, 표 하나도 라인 한 개로 세어집니다.
          </p>
          <p>
            섹션의 회색 헤더에서는 섹션 제목을 바꾸거나, 섹션 자체를 위아래로
            이동·복제·삭제할 수 있습니다. 섹션 안의 각 라인에도 동일한 조작
            버튼이 붙어 있어서, 자주 쓰는 섹션을 복제해 다른 용도로 살짝
            고치거나, 섹션 순서를 바꾸는 일이 몇 번의 클릭으로 끝납니다.
          </p>

          <h4>네 가지 라인 종류</h4>
          <p>
            섹션 바닥의 <strong>+ 텍스트</strong>, <strong>+ HTML/강조</strong>,{' '}
            <strong>+ 표(상단 헤더)</strong>, <strong>+ 표(좌측 라벨)</strong>{' '}
            버튼으로 새 라인을 추가할 수 있습니다. 각각 어떤 상황에 맞는지 짚어
            드리면:
          </p>
          <p>
            <strong>+ 텍스트</strong>는 가장 기본인 한 문단을 넣을 때 씁니다.
            번호를 앞에 붙이고 싶으시면 <code>1.</code> <code>1)</code>{' '}
            <code>①</code> 처럼 직접 타이핑해주시면 됩니다. 에디터가 번호 스타일을
            강제로 통일하지는 않습니다 (원본 문서의 스타일을 그대로 보존하기
            위함입니다).
          </p>
          <p>
            <strong>+ HTML/강조</strong>는 문단 안에 굵은 글씨나 취소선 같은
            꾸밈이 필요할 때 씁니다. <code>&lt;b&gt;강조&lt;/b&gt;</code>,{' '}
            <code>&lt;s&gt;취소선&lt;/s&gt;</code> 처럼 직접 태그를 입력하면
            됩니다. 태그 종류를 외우실 필요는 없고, 이미 있는 비슷한 라인을
            복사해서 텍스트만 바꿔 쓰시면 편합니다.
          </p>
          <p>
            <strong>+ 표(상단 헤더)</strong>는 <em>첫 줄이 열 제목이고 그 아래가
            데이터</em>인 표에 쓰세요. 예를 들어 "구분 | 처리 목적" 처럼 위쪽이
            라벨 행이고 아래에 실제 값이 오는 구조입니다. 제1조의 "구분 / 처리
            목적" 표가 이런 형태입니다.
          </p>
          <p>
            <strong>+ 표(좌측 라벨)</strong>는 <em>왼쪽 열이 라벨</em>인 표에
            쓰세요. "부서 이름 : ◯◯팀 / 이메일 : ◯◯@…" 처럼 왼쪽에 항목명이
            있고 오른쪽에 값이 오는 구조입니다. 제11·12조의 담당 부서·권익침해
            기관 표가 이 형태입니다.
          </p>

          <h4>표 안에서 셀 다루기</h4>
          <p>
            표 라인을 추가하면 각 셀을 클릭해 내용을 입력할 수 있습니다. 마우스를
            셀 위에 올리면 <strong>오른쪽 위 작은 조작 패널</strong>이 보입니다.
          </p>
          <p>
            <strong>L 버튼 (라벨 셀로 지정)</strong> — 일반 셀을 라벨 셀로 바꿔
            주는 토글입니다. 한 번 누르면 배경이 회색으로 바뀌고 버튼이 진하게
            표시되어 "이 셀은 라벨"이라는 것을 알 수 있습니다. 다시 누르면 원래
            셀로 되돌아옵니다. 이 조작은 단순한 시각 강조가 아니라 HTML 의미상
            "옆 셀(들)의 제목이다"라고 선언하는 역할도 해서, 스크린리더 같은
            보조기술에도 올바르게 전달됩니다. 표 맨 윗줄(이미 제목 행으로 묶인
            곳)에는 이 버튼이 나타나지 않는데, 이미 구조상 라벨이라서 중복
            지정이 필요 없기 때문입니다.
          </p>
          <p>
            <strong>r / c 숫자 (셀 병합)</strong> — <code>r</code>은 "아래로 몇
            행에 걸칠지", <code>c</code>는 "옆으로 몇 열에 걸칠지"를 지정합니다.
            숫자를 2 이상으로 올리면 인접한 셀들을 흡수해 하나의 큰 셀이 됩니다.
            되돌리고 싶으면 숫자를 <code>1</code>로 내리거나, 그 옆의{' '}
            <code>×</code> 버튼을 누르세요.
          </p>
          <p>
            <strong>표 아래 행/열 관리 버튼</strong> — 표 하단에 행 추가, 열
            추가, 마지막 행/열 삭제 버튼이 있습니다. 행 추가는 마지막 행과 같은
            구조(셀 개수·병합 상태)를 복사해 새 행을 붙입니다.
          </p>

          <h4>위쪽 요약 배너</h4>
          <p>
            문서를 불러오면 오른쪽 미리보기 패널 위에 <strong>요약 배너</strong>가
            뜹니다. 배너 색만 봐도 상태를 알 수 있도록 되어 있는데,{' '}
            <strong>파란 배너</strong>는 "큰 문제없이 잘 읽었다"는 뜻이고 총 섹션
            수나 표 개수 같은 통계를 같이 보여줍니다. 반대로{' '}
            <strong>노란 배너</strong>는 "점검해볼 만한 부분이 있다"는 신호로,
            스마트따옴표 혼입·번호 매김 혼재·구조가 어긋난 표 같은 구체적인
            이슈가 리스트로 나옵니다.
          </p>
          <p>
            각 이슈 옆에는 <code>§3·L2</code> 같은 작은 칩이 붙어 있습니다. 이걸
            클릭하면 해당 위치로 자동 스크롤되고 잠시 반짝 강조되어 눈에 띄게
            해줍니다. 문제 구간을 직접 찾아다니지 않아도 되도록 도와주는
            장치입니다.
          </p>

          <h4>저장 버튼과 드롭다운</h4>
          <p>
            툴바 오른쪽의 <strong>저장</strong> 버튼은 주 버튼 + 드롭다운(<code>▾</code>)
            한 쌍으로 구성되어 있습니다. 주 버튼은 "가장 자연스러운 동작"을
            한 번에 수행하고, 드롭다운은 모든 형식을 펼쳐서 직접 고를 수 있게
            해 줍니다.
          </p>
          <p>
            <strong>주 버튼</strong> — 현재 JSON 파일이 연결되어 있으면(툴바에{' '}
            <code>● 연결됨</code> 표시가 보일 때) 그 파일에 바로 덮어씁니다.
            연결된 파일이 없으면(DOCX로 열었거나 새로 시작한 경우) 드롭다운
            메뉴가 열립니다.
          </p>
          <p>
            <strong>드롭다운 메뉴 항목</strong> — 다음 다섯 가지에서 선택할 수
            있습니다.
          </p>
          <p>
            <strong>JSON 덮어쓰기</strong>는 현재 연결된 JSON 파일에 덮어씁니다.
            연결된 파일이 없으면 비활성화됩니다. <strong>JSON 다른 이름으로</strong>는
            새 위치·이름으로 <code>.json</code> 파일을 저장합니다.{' '}
            <strong>DOCX 저장</strong>은 Word 문서(<code>.docx</code>)를
            생성합니다. 에디터가 표·헤더 스타일과 문서 구조까지 포함해 완성된
            형태로 내려받습니다. <strong>Markdown 저장</strong>은{' '}
            <code>.md</code> 텍스트로 받습니다. <strong>인쇄 / PDF</strong>는
            미리보기 영역만 인쇄용 레이아웃으로 전환해 브라우저 인쇄 대화상자를
            띄웁니다. 대화상자에서 "PDF로 저장"을 고르시면 PDF로도 받을 수
            있습니다.
          </p>
          <p>
            JSON·DOCX·Markdown은 모두 같은 문서 데이터를 서로 다른 포맷으로
            내보내는 것뿐입니다. 어떤 형식을 다시 열 때 쓰고 어떤 형식을 전달용으로
            쓸지는 프로젝트·팀 상황에 따라 자유롭게 정하시면 되고, 에디터는
            어느 경로든 동등하게 지원합니다 (<code>.json</code>·<code>.docx</code>
            둘 다 다시 읽어 와서 이어서 편집할 수 있습니다). 이 에디터에서
            내려받은 JSON 과 DOCX 는 <strong>서로 완전 호환</strong>됩니다. 어느
            쪽을 다시 열어 저장해도 내용이 동일합니다.
          </p>

          <h4>알아두면 좋은 것</h4>
          <p>
            모든 작업은 <strong>브라우저 안에서만</strong> 이뤄집니다. 파일이
            서버로 업로드되거나 외부로 전송되는 일이 없으니, 민감한 문서라도
            안심하고 작업하실 수 있습니다.
          </p>
          <p>
            외부 Word 에서 직접 받은 원본 파일은 표·들여쓰기·헤더 스타일이 이
            에디터의 규칙과 달라 파싱이 완벽하지 않을 수 있습니다.{' '}
            <strong>한 번 이 에디터로 열어 JSON/DOCX 로 다시 내려받은 파일을
            이후 "기준 파일" 로 사용하세요.</strong> 기준 파일끼리는 포맷 변환을
            몇 번 반복해도 내용이 유지됩니다.
          </p>
        </div>
      </aside>
    </>
  );
}
